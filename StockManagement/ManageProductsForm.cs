using System;
using System.ComponentModel;
using System.Drawing;
using System.Linq;
using System.Windows.Forms;
using System.Data.Entity;
using DataModel;

namespace StockManagement
{
    public partial class ManageProductsForm : Form
    {
        comp1690Entities _context;

        public ManageProductsForm()
        {
            InitializeComponent();
        }

        protected override void OnLoad(EventArgs e)
        {
            base.OnLoad(e);

            // Initialize DB context
            _context = new comp1690Entities();
            _context.Products.Load();
            _context.Warehouses.Load();

            this.productBindingSource.DataSource =
                _context.Products.Local.ToBindingList();

            // Attach custom EditBox to Price column
            var priceColumn = this.dataGridViewTextBoxColumn3;
            var cell = new DataGridViewPriceTextBoxCell();
            priceColumn.CellTemplate = cell;

            // Modify Warehouse column in Stocks grid
            var warehouseColumn = this.DataGridViewComboBoxColumn;
            warehouseColumn.DataPropertyName = "Warehouse_Id";
            warehouseColumn.DataSource = _context.Warehouses.Local.ToBindingList();
            warehouseColumn.DisplayMember = "Name";
            warehouseColumn.ValueMember = "Id";
        }

        private void productBindingNavigatorSaveItem_Click(object sender, EventArgs e)
        {
            this.Validate();

            // Currently, the Entity Framework doesn’t mark the entities
            // that are removed from a navigation property (in our example the Products)
            // as deleted in the context.
            // The following code uses LINQ to Objects against the Local collection
            // to find all products and marks any that do not have
            // a Category reference as deleted.
            // The ToList call is required because otherwise
            // the collection will be modified
            // by the Remove call while it is being enumerated.
            // In most other situations you can do LINQ to Objects directly
            // against the Local property without using ToList first.
            foreach (var stock in _context.Stocks.Local.ToList())
            {
                if (stock.Product == null)
                {
                    _context.Stocks.Remove(stock);
                }
            }

            // Save the changes to the database. 
            this._context.SaveChanges();

            // Refresh the controls to show the values          
            // that were generated by the database. 
            this.stocksDataGridView.Refresh();
            this.productDataGridView.Refresh();

            // Update status label
            UpdateLabel("Saved!", System.Drawing.Color.ForestGreen, true);
        }

        

        private void productDataGridView_CellValueChanged(object sender, DataGridViewCellEventArgs e)
        {
            // Change occured, show it in label
            // Update status label
            UpdateLabel("State updated, save your changes", System.Drawing.Color.DarkOrange);
        }

        private void ManageProductsForm_Shown(object sender, EventArgs e)
        {
            this.productDataGridView.CellValueChanged += 
                new DataGridViewCellEventHandler(productDataGridView_CellValueChanged);
        }

        protected override void OnClosing(CancelEventArgs e)
        {
            base.OnClosing(e);
            this._context.Dispose();
        }

        private void UpdateLabel(string text, Color color, bool flash = false)
        {
            this.Invoke((MethodInvoker)delegate
            {
                this.toolStripStatusLabel1.Text = text;
                this.toolStripStatusLabel1.ForeColor = color;
                this.statusStrip1.Refresh();

                if (flash)
                {
                    System.Threading.Thread.Sleep(1000);
                    this.toolStripStatusLabel1.Text = "";
                    this.statusStrip1.Refresh();
                }
            });
        }
    }
}
